%所有小区内的用户数相同，所有基站、用户的天线个数相同，所有用户的优先权相同
K = 10; %基站数
IK = 1; %一个基站服务的用户数
I = K*IK; %所有的用户数
T = 3; %基站发射天线数
R = 2; %用户接受天线数
SNR = 30; %信噪比
PK = 10^(SNR/10); %每个基站的发射功率上限
d = 1; %原始数据维度

%信道系数服从复标准正态分布,实部虚部独立，均值为零，方差各为1/2的高斯分布
%共有K*I个信道
H = zeros(R,T,K,I);
for k = 1:K
    for i = 1:I
        H(:,:,k,i) = sqrt(1/2)*(randn(R,T)+1j*randn(R,T));
    end
end
%基站k所服务的用户应该表示为i=Ik*(k-1)+ik
[WSR,U,V,W] = functionWMMSE(H,PK,d,K,IK);